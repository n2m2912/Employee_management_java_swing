    private void btDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDeleteActionPerformed
        // TODO add your handling code here:
        if(JOptionPane.showConfirmDialog(this, "Xác nhận xóa ?", "WARNING", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
            int row = tbEmployeeTypes.getSelectedRow();
            id = Integer.valueOf(tbEmployeeTypes.getModel().getValueAt(row, 0).toString());
            bll.deleteEmployeeTypesDAL(id);
            model = (DefaultTableModel) tbEmployeeTypes.getModel();
            model.removeRow(row);
        }
        btEdit.setEnabled(false);
        btDelete.setEnabled(false);
    }//GEN-LAST:event_btDeleteActionPerformed

