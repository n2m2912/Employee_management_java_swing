    public void loadData(){
        int row = 0;
        DefaultTableModel model = (DefaultTableModel) tbEmployeeTypes.getModel();
        List list = bll.readEmployeeTypeBll();
        for (Iterator iterator = list.iterator(); iterator.hasNext();) {
            EmployeeTypes cert = (EmployeeTypes) iterator.next();
            model.addRow(new Object[0]);
            model.setValueAt(cert.getId(), row, 0);
            model.setValueAt(cert.getTypeName(), row, 1);
            row++;
        } 
    }
    
    
